class LocationModel {
  final int id;
  final String name;
  final String province;
  final String code;
  final String value;
  final String label;
  final String desc;
  final String country;

  const LocationModel({
    required this.id,
    required this.name,
    required this.province,
    required this.code,
    required this.value,
    required this.label,
    required this.desc,
    required this.country,
  });
}